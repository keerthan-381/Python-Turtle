t.fillcolor('blue')
t.pencolor('blue')
t.pensize(5)
t.circle(100)
t.end_fill()

t.penup()
t.circle(100,250)
t.pendown()
t.pencolor('white')
t.pensize(15)
t.left(110)
t.forward(185)

t.penup()
t.backward(185)
t.right(110)
